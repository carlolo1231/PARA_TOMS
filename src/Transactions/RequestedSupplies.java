    void loadRequestedSuppliesData() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // Clear existing rows

    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/supplytracker_db", "root", "");
        Statement stmt = con.createStatement();
        ResultSet rs = stmt.executeQuery(
            "SELECT r.request_id, u.fn, u.ln, r.supply_id, r.quantity, r.request_date, r.status " +
            "FROM RequestedSuppliesUser r " +
            "JOIN users u ON r.user_id = u.u_id"
        );

        while (rs.next()) {
            model.addRow(new Object[] {
                rs.getInt("request_id"),
                rs.getString("fn") + " " + rs.getString("ln"), // Full name
                rs.getInt("supply_id"),
                rs.getInt("quantity"),
                rs.getTimestamp("request_date"),
                rs.getString("status")
            });
        }

        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading requested supplies: " + e.getMessage());
    }
}


