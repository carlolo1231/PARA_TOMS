    private void ApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ApproveActionPerformed
       int selectedRow = jTable1.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a request to approve.");
        return;
    }

    int requestId = (int) jTable1.getValueAt(selectedRow, 0); // Column 0 should be request_id
    String currentStatus = jTable1.getValueAt(selectedRow, 5).toString(); // Column 5 should be status

    if (currentStatus.equalsIgnoreCase("approved")) {
        JOptionPane.showMessageDialog(this, "This request is already approved.");
        return;
    }

    if (currentStatus.equalsIgnoreCase("rejected")) {
        JOptionPane.showMessageDialog(this, "This request has been rejected and cannot be approved.");
        return;
    }

    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/supplytracker_db", "root", "");
        String sql = "UPDATE RequestedSuppliesUser SET status = 'approved' WHERE request_id = ?";
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setInt(1, requestId);

        int updated = ps.executeUpdate();
        if (updated > 0) {
            JOptionPane.showMessageDialog(this, "Request approved successfully.");
            loadRequestedSuppliesData(); // Refresh the table
        } else {
            JOptionPane.showMessageDialog(this, "Failed to approve the request.");
        }

        con.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
    }
    }//GEN-LAST:event_ApproveActionPerformed

