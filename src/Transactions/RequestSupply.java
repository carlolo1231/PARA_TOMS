    private void RequestSupplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RequestSupplyActionPerformed
      int supplyId;
    int quantity;

    try {
        supplyId = Integer.parseInt(txtSupplyId.getText().trim());
        quantity = Integer.parseInt(txtQuantity.getText().trim());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter valid numeric values for Supply ID and Quantity.");
        return;
    }

    if (quantity <= 0) {
        JOptionPane.showMessageDialog(this, "Quantity must be greater than zero.");
        return;
    }

    int userId = Session.getInstance().getuid(); // Get user ID from session

    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/supplytracker_db", "root", "");

        // Check if supply exists and get current quantity
        String checkSupply = "SELECT quantity FROM supplies WHERE supply_id = ?";
        PreparedStatement psCheck = con.prepareStatement(checkSupply);
        psCheck.setInt(1, supplyId);
        ResultSet rsCheck = psCheck.executeQuery();

        if (!rsCheck.next()) {
            JOptionPane.showMessageDialog(this, "Supply ID not found.");
            con.close();
            return;
        }

        int currentQuantity = rsCheck.getInt("quantity");

        if (quantity > currentQuantity) {
            JOptionPane.showMessageDialog(this, "Not enough supply available. Current quantity: " + currentQuantity);
            con.close();
            return;
        }

        // Insert the request with 'pending' status
        String insertRequest = "INSERT INTO RequestedSuppliesUser (user_id, supply_id, quantity, status) VALUES (?, ?, ?, 'pending')";
        PreparedStatement pst = con.prepareStatement(insertRequest);
        pst.setInt(1, userId);
        pst.setInt(2, supplyId);
        pst.setInt(3, quantity);
        int rowsInserted = pst.executeUpdate();

        if (rowsInserted > 0) {
            // Deduct the quantity from the supplies table
            String updateSupply = "UPDATE supplies SET quantity = quantity - ? WHERE supply_id = ?";
            PreparedStatement psUpdate = con.prepareStatement(updateSupply);
            psUpdate.setInt(1, quantity);
            psUpdate.setInt(2, supplyId);
            psUpdate.executeUpdate();

            JOptionPane.showMessageDialog(this, "Supply request submitted. Awaiting admin approval.");
            txtSupplyId.setText("");
            txtQuantity.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Failed to request supply.");
        }

        con.close();

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
    }
    }//GEN-LAST:event_RequestSupplyActionPerformed

